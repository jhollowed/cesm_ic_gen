!=======================================================================
!  initialize the horizontal and vertical grid
!=======================================================================
   subroutine initialize_grid
     
     use homme_lat_lon
     use cubedsphere_lat_lon
     implicit none

!-----------------------------------------------------------------------
!    local variables
!-----------------------------------------------------------------------
     integer i, j
     real*8 dy, dy_half,                                            & ! latitudinal spacing
            dx, dx_half                                               ! longitudinal spacing

     real*8 :: sin_lat_np,                                          &
               cos_lat_np,                                          &
               rotated_lat

      real(r8) :: T0,                                               & !  reference temperature
                  H,                                                & !  scale height
                  S,                                                & !  parameter
                  N2,                                               & !  squared Brunt-Vaisala frequency
                  gamma,                                            & !  adiabatic lapse rate gamma = g/cp
                  p_top,                                            & !  pressure at the model top in Pa
                  z_top,                                            & !  height at model top in m
                  eta_top,                                          & !  eta_top = p_top/p0
                  height,                                           & !  height in m
                  scale_height                                        !  scale height

!-----------------------------------------------------------------------
!     allocate arrays
!-----------------------------------------------------------------------
      if (model_version.eq.1 .or.model_version.eq.2)                & ! EUL, SLD, & FV 
      allocate (lat(nlat),                                          &
                lon(nlon),                                          &
                weight(nlat))
      if (model_version.eq.2)                                       & ! FV
      allocate (slat(nslat),                                        &
                slon(nlon))
      if (model_version.eq.3)                                       & ! SE
      allocate (lat(ncol),                                          &
                lon(ncol),                                          &
                weight(ncol))
      if (model_version.eq.4)                                       & ! FV3
      allocate (lat(ncol),                                          &
                lon(ncol),                                          &
                weight(ncol))

!-----------------------------------------------------------------------
!     allocate vertical arrays
!-----------------------------------------------------------------------
      allocate (hyam(nlev),                                         & ! coefficient a, full level
                hybm(nlev),                                         & ! coefficient b, full level
                eta (nlev),                                         &
                hyai(nlev+1),                                       & ! coefficient a, interface level
                hybi(nlev+1),                                       & ! coefficient b, interface level
                etai (nlev+1),                                      &
                lev(nlev),                                          &
                ilev(nlev+1))

!-----------------------------------------------------------------------
!     initialize the vertical grid
!-----------------------------------------------------------------------
      if (nlev == 30) then                                         ! standard CAM5 30 levels 
        hyai = hyai_L30
        hybi = hybi_L30
        hyam = hyam_L30
        hybm = hybm_L30
      else if (nlev == 52) then                                   ! standard CAM5 30 levels 
        hyai = hyai_L52
        hybi = hybi_L52
        hyam = hyam_L52
        hybm = hybm_L52
      else if (nlev == 60) then                                   ! standard CAM5 30 levels 
        hyai = hyai_L60
        hybi = hybi_L60
        do i = 1, nlev
          hyam(i) = 0.5_r8 * (hyai(i+1) + hyai(i))                    ! a coefficient at full levels
          hybm(i) = 0.5_r8 * (hybi(i+1) + hybi(i))                    ! b coefficient at full levels
        enddo
      else if (nlev == 70) then                                   ! standard CAM5 30 levels 
        hyai = hyai_L70
        hybi = hybi_L70
        hyam = hyam_L70
        hybm = hybm_L70
      elseif (choice_testcase .eq. 31 .and. nlev == 10) then
!        hyai = hyai_L10
!        hybi = hybi_L10
!        hyam = hyam_L10
!        hybm = hybm_L10
          T0    = 300._r8                                               !  temperature at the surface
          dz    = 1000._r8                                              !  vertical grid spacing
          z_top = nlev*dz            
          N2 = 0.0001_r8                                                !  Brunt-Vaisala frequency squared
          S = g*g/(cp*N2)                                               !  parameter
          p_top = p0 * ( (1._r8 - S/T0) + S/T0 * exp(- N2*z_top/g) )**(cp/Rd)  !  pressure position of the model top
          eta_top = p_top/p0                                            ! eta position of the model top
          
          do i = nlev+1, 1, -1
            height  = (nlev+1 - i)*dz                                   ! height at the interface
            ilev(i) = p0 * ( (1._r8 - S/T0) + S/T0 * exp(- N2*height/g))**(cp/Rd) ! pressure at the interface in Pa
            etai(i) = ilev(i)/p0                                        ! eta at the interface
            ilev(i) = ilev(i)*0.01_r8                                   !  convert pressure to hPa
            hybi(i) = (etai(i) - eta_top) / (1._r8 - eta_top)           ! a coefficient at the interface levels
            hyai(i) = etai(i) - hybi(i)                                 ! b coefficient at the interface levels
          enddo   

          do i = 1, nlev
            hyam(i) = 0.5_r8 * (hyai(i+1) + hyai(i))                    ! a coefficient at full levels
            hybm(i) = 0.5_r8 * (hybi(i+1) + hybi(i))                    ! b coefficient at full levels
            lev(i)  = 0.5_r8 * (ilev(i+1) + ilev(i))                    ! pressure at the full level in hPa
          enddo
      elseif (choice_testcase .eq. 11 .and. nlev == 60) then
         hyai = hyai_L60_adv
         hybi = hybi_L60_adv
         hyam = hyam_L60_adv
         hybm = hybm_L60_adv
!          T0    = 300._r8                                               ! temperature at the surface
!          dz    = 200._r8                                               ! vertical grid spacing
!          z_top = nlev*dz                                               ! height position of model top
!          scale_height = Rd*T0/g                                        ! scale height
!          p_top = p0 *  exp(- z_top/scale_height)                       ! pressure position of the model top
!          write (*,*) 'p_top:', p_top
!          eta_top = p_top/p0                                            ! eta position of the model top
          
          do i = nlev+1, 1, -1
!            height  = (nlev+1 - i)*dz                                   ! height at the interface
!            ilev(i) = p0 * exp(- height/scale_height)                   ! pressure at the interface in Pa
            ilev(i) = p0*(hyai(i) + hybi(i))                             ! pressure at the interface in Pa
!            etai(i) = ilev(i)/p0                                         ! eta at the interface
            ilev(i) = ilev(i)*0.01_r8                                    !  convert pressure to hPa
!            hybi(i) = (etai(i) - eta_top) / (1._r8 - eta_top)           ! a coefficient at the interface levels
!            hyai(i) = etai(i) - hybi(i)                                 ! b coefficient at the interface levels
          enddo   

          do i = 1, nlev
!            hyam(i) = 0.5_r8 * (hyai(i+1) + hyai(i))                    ! a coefficient at full levels
!            hybm(i) = 0.5_r8 * (hybi(i+1) + hybi(i))                    ! b coefficient at full levels
            lev(i)  = 0.5_r8 * (ilev(i+1) + ilev(i))                    ! pressure at the full level in hPa
          enddo
!          write (*,*) 'ilev:', ilev
!          write (*,*)
!          write (*,*) 'lev:', lev
      end if

      etai(1) = hyai(1) + hybi(1)              ! initialize the first hybrid interface level (model top)
      do i = 1, nlev
        eta(i) = hyam(i) + hybm(i)             ! initialize the hybrid full levels
        etai(i+1) = hyai(i+1) + hybi(i+1)      ! initialize the hybrid interface levels
        lev(i) = eta(i)*p0*0.01_r8             ! initialize the pressure on the reference grid in hPa
        write (*,*) 'eta (',i,'): ',eta(i)
        write (*,*) 'lev (',i,'): ',lev(i)
      enddo
!-----------------------------------------------------------------------
!     initialize the horizontal grid
!-----------------------------------------------------------------------

      !-----------------------------------------------------------------------
      !       Gaussian grid (Eulerian and semi-Lagrangian)
      !-----------------------------------------------------------------------
      if (model_version.eq.1) then
        dx = 2.d0*pi/dble(nlon)
        
        ! ---------- longitudes ----------
        do i = 1, nlon
          lon(i) = (i-1)*dx
        enddo
        ! ---------- latitudes ----------
        do i = 1, nlat
          lat(i)    = gaussian_latitudes(i) / convert                  ! convert to radians
          weight(i) = gaussian_weights(i) * 0.5d0                      ! normalize
        enddo
      

      !-----------------------------------------------------------------------
      !       FV Lin-Rood staggered grid
      !-----------------------------------------------------------------------
      else if (model_version.eq.2) then
        dx = 2.d0*pi/dble(nlon)
        dx_half = dx*0.5d0
        dy = pi/dble(nslat)
        dy_half = dy*0.5d0
        slon(1) = 2.d0*pi - dx_half
        lon (1) = 0.d0
        
        ! ---------- longitudinal direction (radians) ----------
        do i = 2, nlon
          lon(i)  = (i-1)*dx
          slon(i) = lon(i) - dx_half
        enddo
        
        ! ---------- meridional direction (radians) ----------
        do i = 1, nslat
          lat(i)  = -pih + (i-1)*dy
          slat(i) = lat(i) + dy_half
        enddo
        lat(nlat) = -pih + (nslat)*dy

        ! ----------  Normalized integration weights ----------
        !   (for the calculation of the mean values per level)
        do i = 2, nslat
          weight(i)  = 0.5d0*(sin(slat(i)) - sin(slat(i-1)))
        enddo
        weight(1)    = 0.5d0*(sin(slat(1)) + 1.d0)
        weight(nlat) = weight(1)

      !-----------------------------------------------------------------------
      !       SE grid, longitudinal and latitude (radians)
      !-----------------------------------------------------------------------
      else if (model_version.eq.3) then
        do i = 1, ncol
          lat(i)    = homme_latitudes(i) / convert                  ! convert to radians
          lon(i)    = homme_longitudes(i) / convert                 ! convert to radians
          weight(i) = 0.d0
        enddo
      
      !-----------------------------------------------------------------------
      !       FV3 grid, longitudinal and latitude (radians)
      !-----------------------------------------------------------------------
      else if (model_version.eq.4) then
        do i = 1, ncol
          lat(i)    = cubedsphere_latitudes(i) / convert                  ! convert to radians
          lon(i)    = cubedsphere_longitudes(i) / convert                 ! convert to radians
          weight(i) = 0.d0
        enddo
      endif
   end subroutine initialize_grid


